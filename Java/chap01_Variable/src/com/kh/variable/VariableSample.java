package com.kh.variable;

public class VariableSample {
	public static void main (String[] args) {
		String name;
		name = "쿠팡";
		
		int hour = 15;
		double score = 90.5;
		char grade = 'A';
		
		System.out.println("안녕하세요. " + name + "입니다. 오늘 " + hour + "시에 배송됩니다.");
		System.out.println("저의 성적은 " + score + " 입니다.");
		System.out.println(score + "은 학점 " + grade + " 입니다.");
		
		//변수에 이름과 나이와 자신의 성적 (double)으로 넣기
		String nm = "안승애";
		int age = 23;
		double scr = 80.5;
		char grd = 'B';
		System.out.println(nm + "의 나이는 " + age + "입니다.");
		System.out.println(nm + "의 성적은 " + scr+ "이므로 학점은 " + grd + "입니다.");
		
			
	}
		

}












